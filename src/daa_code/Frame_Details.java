    private void B_DetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_DetailsActionPerformed
        String S_Location = Combo_Loct.getSelectedItem().toString();            //Definition and Declaration
        char C_Location = S_Location.charAt(0); 
        String [] S_TblData= new String[4]; 
        int I_Speed;
        DefaultTableModel DTM_dets =(DefaultTableModel)T_1.getModel();
        File F_text = new File("C:\\Users\\JM\\Desktop\\Data.txt");
        DTM_dets.setRowCount(0);//Removal of Rows
         try{
            BufferedReader BR = new BufferedReader(new FileReader(F_text));    //Setting File to BufferedReader
            String line=BR.readLine();  
            while(!(line.charAt(0)=='-'&&line.charAt(1)==C_Location)){         //Moving BufferedReader to Chosen Location
                line=BR.readLine(); 
            }
            line=BR.readLine();   //Acquiring Location's data
            do{
                S_TblData = line.split("/", 4);
                for(int i=0; i <4; i++){
                    S_TblData[i]=S_TblData[i].trim();
                    if(i==3){
                        I_Speed=Integer.parseInt(S_TblData[3]);
                        S_TblData[3] = Integer.toString(I_Speed/60)+" hr/s "+Integer.toString(I_Speed%60)+" min/s";
                    }
                }
                DTM_dets.addRow(S_TblData);
                line=BR.readLine();
            }while(line.charAt(0)!='=');   
         }
         catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
         }
    }//GEN-LAST:event_B_DetailsActionPerformed

